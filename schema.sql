-- Sensores y Zonas de Monitoreo Industrial
-- Se diseña la relacion muchos-a-muchos entre `sensors` y `zones`
-- mediante la tabla intermedia "monitorings".

DROP TABLE IF EXISTS monitorings CASCADE;
DROP TABLE IF EXISTS sensors CASCADE;
DROP TABLE IF EXISTS zones CASCADE;

-- Tabla: sensors
CREATE TABLE sensors (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre            VARCHAR(120) NOT NULL,
    tipo              VARCHAR(20)  NOT NULL
                          CHECK (tipo IN ('temperatura', 'presion', 'vibracion', 'flujo')),
    fabricante        VARCHAR(120) NOT NULL,
    fecha_fabricacion DATE         NOT NULL,
    created_at        TIMESTAMPTZ  NOT NULL DEFAULT now()
);

-- Tabla: zones
CREATE TABLE zones (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre           VARCHAR(120) NOT NULL,
    descripcion      TEXT         NOT NULL,
    ubicacion        VARCHAR(150) NOT NULL,
    estado_operativo VARCHAR(20)  NOT NULL DEFAULT 'operativa'
                         CHECK (estado_operativo IN ('operativa', 'mantenimiento', 'inactiva')),
    created_at       TIMESTAMPTZ  NOT NULL DEFAULT now()
);

-- Tabla: monitorings (Tabla intermedia)
CREATE TABLE monitorings (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sensor_id         BIGINT       NOT NULL REFERENCES sensors(id) ON DELETE CASCADE,
    zone_id           BIGINT       NOT NULL REFERENCES zones(id)   ON DELETE CASCADE,
    fecha_instalacion DATE         NOT NULL DEFAULT CURRENT_DATE,
    tipo_lectura      VARCHAR(20)  NOT NULL
                          CHECK (tipo_lectura IN ('temperatura', 'presion', 'vibracion', 'flujo')),
    valor_umbral      NUMERIC(12,2) NOT NULL,
    valor_actual      NUMERIC(12,2),
    estado_monitoreo  VARCHAR(20)  NOT NULL DEFAULT 'activo'
                          CHECK (estado_monitoreo IN ('activo', 'pausado')),
    created_at        TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at        TIMESTAMPTZ  NOT NULL DEFAULT now(),

    -- Se impide la asignacion duplicada exacta
    CONSTRAINT uq_monitoring_sensor_zone_lectura
        UNIQUE (sensor_id, zone_id, tipo_lectura)
);

-- Indices:
-- PostgreSQL no indexa las FK automaticamente. Se agregan indices
-- para acelerar las consultas GET /sensors/:id/zones y
-- GET /zones/:id/sensors. El indice sobre sensor_id ya esta cubierto
-- por el prefijo izquierdo de la restriccion UNIQUE, por lo que aqui
-- solo se requiere el de zone_id.

CREATE INDEX idx_monitorings_zone_id ON monitorings (zone_id);
CREATE INDEX idx_monitorings_estado  ON monitorings (estado_monitoreo);

-- Trigger: mantener updated_at en monitorings
CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_monitorings_updated_at
    BEFORE UPDATE ON monitorings
    FOR EACH ROW
    EXECUTE FUNCTION set_updated_at();

